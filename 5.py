 #Задание №5 Данн массив (например [1, 2, 3, 4] представляющий число 1234. Прибавить к нему «Единицу» (например [1, 2, 3, 5]) Еще примеры: [1, 9, 9] + 1 = [2, 0, 0] [9] + 1 = [1, 0]
m = [9, 9]

s = ''
for n in m:
    s += str(n)
k = int(s) + 1
q = []
while k > 0:
    q.append(k%10)
    k //= 10
print(m, '+ 1 =', q[::-1])